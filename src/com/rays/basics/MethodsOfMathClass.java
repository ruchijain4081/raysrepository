package com.rays.basics;

public class MethodsOfMathClass {
public static void main(String[] args) {
	System.out.println("mathematics operation by math class methods");
	System.out.println("maximum of 4,2 = "+Math.max(4, 2));
	System.out.println("minimum of 8,5 = "+Math.min(8,5));
	System.out.println("absolute vlaue of 10.6 = "+Math.abs(10.6));
	System.out.println("random numbers = "+Math.random()*1000);
	System.out.println("square root of 6 = "+Math.sqrt(9));
	System.out.println("exponential of 10 = "+Math.exp(2));
}
}
